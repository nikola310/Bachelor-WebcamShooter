package com.webcamshooter.controller;

public class CameraShooterException extends RuntimeException {

	/**
	 * 
	 */
	private static final long serialVersionUID = 8059119891219219255L;

	public CameraShooterException() {
		super();

	}

	public CameraShooterException(String message, Throwable cause, boolean enableSuppression, boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);

	}

	public CameraShooterException(String message, Throwable cause) {
		super(message, cause);

	}

	public CameraShooterException(String message) {
		super(message);

	}

	public CameraShooterException(Throwable cause) {
		super(cause);

	}

}
